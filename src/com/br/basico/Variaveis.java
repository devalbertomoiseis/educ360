package com.br.basico;

public class Variaveis {
    public static void main(String[] args) {

        byte numeroInteiroMuitoPequen = 127;
        short numeroInteiroPequeno = 12345;
        int numeroInteiro = 123456789;
        long numeroInteiroGrande = 123456789123456L;
        float numeroReal = 12345678F;
        double numeroRealGrade = 123456789;

        System.out.println("byte : " + numeroInteiroMuitoPequen);
        System.out.println("short : " + numeroInteiroPequeno);
        System.out.println("int : " + numeroInteiro);
        System.out.println("long : " + numeroInteiroGrande);
        System.out.println("float : " + numeroReal);
        System.out.println("double : " + numeroRealGrade);
        System.out.println("Hello world!");
    }
}
